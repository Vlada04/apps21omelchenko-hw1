    @Test
    public void testfindLess() {
        double tempValue = 2.0;
        double[] temperatureSeries = {3.0, -5.0 , 5.0, -1.0};
        TemperatureSeriesAnalysis seriesAnalysis = new TemperatureSeriesAnalysis(temperatureSeries);
        double[] expResult = new double[2];
        expResult[0] = -5.0;
        expResult[1] = -1.0;

        double[] actualResult = seriesAnalysis.findTempsLessThen(tempValue);

        assertEquals(expResult[0], actualResult[0], 0.00001);
        assertEquals(expResult[1], actualResult[1], 0.00001);
    }

